var LocaleHelper = {};

LocaleHelper.localeToCountry = function (locale) {
	var id = locale.split("_")[1];

	for (var i = 0; i < this.codes_length; i += 1) {
		var item = this.codes[i];

		if (item.id === id) {
			return item.value;
		}
	}
	// Country code not recognised
	return "";
};

